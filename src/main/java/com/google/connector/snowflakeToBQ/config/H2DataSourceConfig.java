package com.google.connector.snowflakeToBQ.config;

import javax.sql.DataSource;
import lombok.Getter;
import lombok.Setter;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.boot.jdbc.DataSourceBuilder;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.Primary;

/**
 * Configuration class to create a datasource for the property defined with a prefix
 * "spring.datasource.h2" in application.properties. Properties are H2 embedded database properties.
 */
@Configuration
@ConfigurationProperties(prefix = "spring.datasource.h2")
@Setter
@Getter
public class H2DataSourceConfig {
  private String url;
  private String username;
  private String password;
  private String driverClassName;

  @Primary
  @Bean
  public DataSource h2DataSource() {
    return DataSourceBuilder.create()
        .driverClassName(driverClassName)
        .url(url)
        .username(username)
        .password(password)
        .build();
  }
}
